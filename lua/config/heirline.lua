local FileNameBlock, CurrentPath, FileName
do
  local FileIcon = {
    condition = function()
      return not ReadOnly.condition()
    end,
    init = function(self)
      local filename = self.filename
      local extension = fn.fnamemodify(filename, ":e")
      self.icon, self.icon_color = devicons.get_icon_color(filename, extension, { default = true })
    end,
    provider = function(self)
      if self.icon then
        return self.icon .. " "
      end
    end,
    hl = function(self)
      return { fg = self.icon_color }
    end,
  }

  local WorkDir = {
    init = function(self)
      self.icon = (vim.fn.haslocaldir(0) == 1 and "l" or "g") .. " " .. "Ôêì "
      local cwd = vim.fn.getcwd(0)
      self.cwd = vim.fn.fnamemodify(cwd, ":~")
    end,
    hl = { fg = "blue", bold = true },

    flexible = 1,

    {
      -- evaluates to the full-lenth path
      provider = function(self)
        local trail = self.cwd:sub(-1) == "/" and "" or "/"
        return self.icon .. self.cwd .. trail .. " "
      end,
    },
    {
      -- evaluates to the shortened path
      provider = function(self)
        local cwd = vim.fn.pathshorten(self.cwd)
        local trail = self.cwd:sub(-1) == "/" and "" or "/"
        return self.icon .. cwd .. trail .. " "
      end,
    },
    {
      -- evaluates to "", hiding the component
      provider = "",
    },
  }

  CurrentPath = {
    condition = function(self)
      if bo.buftype == "" then
        return self.current_path
      end
    end,
    hl = { fg = utils.get_highlight("Directory").fg },
    flexible = 2,
    {
      provider = function(self)
        return self.current_path
      end,
    },
    {
      provider = function(self)
        return fn.pathshorten(self.current_path, 2)
      end,
    },
    { provider = "" },
  }

  FileName = {
    provider = function(self)
      return self.filename
    end,
    hl = { fg = utils.get_highlight("Statusline").fg, bold = true },
  }

  FileNameBlock = {
    { FileIcon, WorkDir, CurrentPath, FileName },
    -- This means that the statusline is cut here when there's not enough space.
    { provider = "%<" },
  }
end

